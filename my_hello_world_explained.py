_ = 0B0000001 # 1
__ = 0b0000011 # 3
___ = 0b0000100 # 4
____ = 0b0000101 # 5
_____ = 0b1111111 # 127

# & bitwise and
# | bitwise or
# ^ bitwise xor
# ~ bitwise invert
# << bitwise left shift
# >> bitwise right shift

hello_world = [
    (_ ^ __) << ____ | _ << __, # 0b1001000 -> 72 -> H
    __ << ____ | _ << (_ ^ __) | _, # 0b1100101 -> 101 -> e
    _____ ^ __ ^ _ << ___, # 0b1101100 -> 108 -> l
    ~(_ << ___ | __) & _____, # 0b1101100 -> 108 -> l
    _____ ^ _ << ___ | __, # 0b1101111 -> 111 -> o
    _ << ____, # 0b100000 -> 32 -> ' '
    ____ << ___ | __ | ____, # 0b1010111 -> 87 -> W
    ~(_ << ___) & _____, # 0b1101111 -> 111 -> o
    ~(__ << _ << _ | _) & _____, # 0b1110010 -> 114 -> r
    _____ ^ (_ << ___ | __), # 0b1101100 -> 108 -> l
    __ << ____ | _ ^ ____, # 0b1100100 -> 100 -> d
    _ << ____ | _, # 0b100001 -> 33 -> !
]

print(''.join(chr(_) for _ in hello_world))